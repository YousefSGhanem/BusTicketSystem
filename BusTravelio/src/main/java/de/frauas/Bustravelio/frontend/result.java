        DBHandler tt = new DBHandler();
        public void showTravels(){
         ArrayList<Travels> list = tt.travellist(From, To,datum,seat);
          model = (DefaultTableModel)jTable1.getModel();
         Object [] row = new Object[8];
            for(int i =0; i<list.size();i++){
                row[0]=list.get(i).getId();
                row[1]=list.get(i).getfromcity();
                row[2]=list.get(i).gettocity();
                row[3]=list.get(i).getdatum();
                row[4]=list.get(i).getdepartureTime();
                row[5]=list.get(i).getarriveTime();
                row[6]=list.get(i).getprice()+price;
                row[7]=list.get(i).getseat();
                model.addRow(row);
            }
        }
